void LightObj::Render()
{
    glm::mat4 MVP = glm::mat4(1.0f);
    model = glm::mat4(1.0f);
    model = glm::translate(model, lightPos);
    model = glm::scale(model, glm::vec3(0.2f, 0.2f, 0.2f));
    MVP = projection * view * model;
    lightShader->Use();
    lightShader->setMat4("MVP", MVP);
    glBindVertexArray(lightModel->getVAOID());
    glDrawArrays(GL_TRIANGLES, 0 , lightModel->getVertexCount());
}

void LightObj::setLightPos(const glm::vec3 &LightPosition)
{
    lightPos = LightPosition;
}

void LightObj::setViewMatrix(const glm::mat4 &view)
{
    this->view = view;
}

void LightObj::setProjectionMatrix(const glm::mat4 &projection)
{
    this->projection = projection;
}

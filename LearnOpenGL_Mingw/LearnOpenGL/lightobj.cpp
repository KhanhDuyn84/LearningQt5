#include "lightobj.h"
#include "glslshader.h"
#include "model.h"
#include "filehelper.h"
#include "glm/gtc/matrix_transform.hpp"

using namespace GSEngine;

LightObj::LightObj()
{
    fileHelper = FileHelper::getInstance();
    InitTexture();
    InitShader();
    InitModel();
    lightShader->Use();
    lightShader->setVec3("lightColor",  glm::vec3(1.0, 1.0, 1.0));
}

LightObj::~LightObj()
{
    if(lightModel)
    {
        delete lightModel;
        lightModel = nullptr;
    }
    if(lightShader)
    {
        delete lightShader;
        lightShader = nullptr;
    }
}

void LightObj::InitShader()
{
    std::string objectVSPath    = fileHelper->getPath("OBJECT_VS");
    std::string lampFSPath    = fileHelper->getPath("LAMP_FS");
    lightShader = new GLSLShader(objectVSPath.c_str(), lampFSPath.c_str());
    lightShader->Use();
    lightShader->addUniform("lightColor");
    lightShader->addUniform("MVP");
}

